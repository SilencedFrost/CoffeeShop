    public void showProduct(String productName)
    {
        pd = Product_DAO.getProductInfo(productName);
        ((ImageLabel)imgProduct).setImage(pd.getPicture());
        lblPdName.setText(pd.getProductName());
        lblPrice.setText(pd.getPriceS());
        if(pd.isS()) {cboSize.addItem("S");}
        if(pd.isM()) {cboSize.addItem("M");}
        if(pd.isL()) {cboSize.addItem("L");}
        lblDesc.setText(pd.getProductDesc());
    }
