    private void cboSizeItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboSizeItemStateChanged
        switch(cboSize.getSelectedItem().toString())
        {
            case "S" ->
            {
                lblPrice.setText(pd.getPriceS());
            }
            case "M" ->
            {
                lblPrice.setText(pd.getPriceM());
            }
            case "L" ->
            {
                lblPrice.setText(pd.getPriceL());
            }
        }
    }//GEN-LAST:event_cboSizeItemStateChanged

